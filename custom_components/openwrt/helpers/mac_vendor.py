def get_mac_vendor_info(mac: str) -> tuple[str, str] | None:
    """Get manufacturer and model from MAC address."""
    if not mac:
        return None
    # Normalization: upper case and colon separator for the first 8 chars (OUI)
    clean_mac = mac.replace("-", ":").upper()

    # Check for randomized/private MAC address
    # (locally administered if bit 1 of the first byte is 1)
    # This corresponds to the second hex digit being 2, 6, A, or E.
    if len(clean_mac) >= 2 and clean_mac[1] in "26AE":
        return ("Private MAC", "Randomized Address")

    prefix = clean_mac[:8]
    return MAC_VENDOR_MAP.get(prefix)
